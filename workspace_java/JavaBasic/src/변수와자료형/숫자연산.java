package 변수와자료형;

public class 숫자연산 {
    public static void main(String[] args) {



        //정수끼리의 연산 결과는 정수(소수x)로 나옴
        System.out.println(10 / 4 );

        //실수끼리의 연산 결과는 무조건 실수(소수)로 나옴
        System.out.println(10.0 / 4.0 );

        //★★실수가 포함된 연산은 실수로 나옴
        System.out.println(10 / 4.0);


        System.out.println(10 / 3 * 1.5);


    }
}
