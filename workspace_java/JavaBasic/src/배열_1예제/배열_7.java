package 배열_1예제;

public class 배열_7 {
    public static void main(String[] args) {

        int[] arr1 = new int[10];


        // 모든 요소에 3을 저장
        for(int i = 0 ; i < arr1.length ; i++){
            arr1[i] = 3;
        }


        //모든 요소를 출력
        for(int i = 0 ; i < arr1.length ; i++){

            //배열의 ? 번째 요소를 출력
            System.out.println(arr1[i]);
        }









    }
}
