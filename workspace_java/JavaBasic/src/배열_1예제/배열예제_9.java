package 배열_1예제;

public class 배열예제_9 {


    public static void main(String[] args) {


        int[] arr1 ={1,5,7};
        int sum = 0;

        for (int i = 0; i <arr1.length ;  i++){

            //배열의 각 요소의 합
            sum = sum + arr1[i];

            System.out.println(sum);


        }




    }

}
