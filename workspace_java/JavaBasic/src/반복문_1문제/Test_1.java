package 반복문_1문제;

public class Test_1 {
    public static void main(String[] args) {

        int i = 1;
        // 1번 : i에 대입
        while (i <= 5){
            //2번 반복문으로 i실행
            System.out.print("*");
            //3번 *을 출력
            i++;
            //4번 *이 조건을 만족할 때 까지 반복
        }



    }
}
